-- CREATE ALL TRIGGERS START
CREATE OR REPLACE TRIGGER trg_SetDueDate
BEFORE INSERT ON Loans
FOR EACH ROW
BEGIN
    :NEW.DueDate := :NEW.IssueDate + 14;
END;
/

CREATE OR REPLACE TRIGGER trg_UpdateCopiesAvailable
AFTER INSERT OR UPDATE OF ReturnDate ON Loans
FOR EACH ROW
BEGIN
    IF INSERTING THEN
        UPDATE Books
        SET CopiesAvailable = CopiesAvailable - 1
        WHERE BookID = :NEW.BookID;
        
    ELSIF UPDATING THEN
        UPDATE Books
        SET CopiesAvailable = CopiesAvailable + 1
        WHERE BookID = :NEW.BookID;
    END IF;
END;
/

