-- Procedure that adds the author and book to the database
create or replace PROCEDURE AddBook (
    p_BookID NUMBER,
    p_Title VARCHAR2,
    p_Author VARCHAR2,
    p_Publisher VARCHAR2,
    p_Year NUMBER,
    p_Genre VARCHAR2,
    p_Copies NUMBER,
    p_cover VARCHAR2
)
AS
    v_AuthorID Author.AuthorID%TYPE;
    v_AuthorCheck varchar(100);
BEGIN
    BEGIN
        -- Looks to see if the author is already in the table        
        SELECT AuthorID
        INTO v_AuthorID
        FROM Author
        WHERE AuthorName = p_Author;
    EXCEPTION
        -- If there is no author, then it will add one
        WHEN NO_DATA_FOUND THEN
            INSERT INTO Author(AuthorName, AuthorBio)
            VALUES (p_Author, null);
    END;

    -- Gets the authorid from author name
    SELECT AuthorID
    INTO v_AuthorID
    FROM Author
    WHERE AuthorName = p_Author;
    
    -- Adds book to table
    INSERT INTO Books(BookID, Title, AuthorID, Publisher, Year, Genre, CopiesAvailable, cover)
    VALUES (p_BookID, p_Title, v_AuthorID, p_Publisher, p_Year, p_Genre, p_Copies, p_cover);
END;
/


-- Procedure that adds a member to the database
create or replace PROCEDURE AddMember (
    p_MemberName VARCHAR2,
    p_MemberAddress VARCHAR2,
    p_MemberPhone VARCHAR2,
    p_MemberEmail VARCHAR2,
    p_MemberPassword VARCHAR2 DEFAULT NULL
)
AS
BEGIN
    INSERT INTO Members(MemberName, MemberAddress, MemberPhone, MemberEmail, MemberPassword)
    VALUES (p_MemberName, p_MemberAddress, p_MemberPhone, p_MemberEmail, NVL(p_MemberPassword, 'password'));
END;
/


