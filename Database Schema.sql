-- CREATE ALL TABLES START
CREATE TABLE Author (
    AuthorID        NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) PRIMARY KEY,
    AuthorName      VARCHAR2(100),
    AuthorBio       VARCHAR2(4000)
);

CREATE TABLE Members (
    MemberID       NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) PRIMARY KEY,
    MemberName     VARCHAR2(100) NOT NULL,
    MemberAddress  VARCHAR2(200),
    MemberPhone    VARCHAR2(15),
    MemberEmail    VARCHAR2(100),
    MemberPassword VARCHAR2(20) DEFAULT 'password'
);

CREATE TABLE Staff (
    StaffID       NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) PRIMARY KEY,
    StaffName     VARCHAR2(100),
    StaffRole     VARCHAR2(20),
    StaffPhone    VARCHAR2(15),
    StaffEmail    VARCHAR2(100),
    HireDate DATE,
    StaffPassword VARCHAR2(20) DEFAULT 'password'
);

CREATE TABLE Books (
    BookID          NUMBER PRIMARY KEY,
    Title           VARCHAR2(100) NOT NULL,
    AuthorID        REFERENCES Author(AuthorID),
    Publisher       VARCHAR2(100),
    Year            NUMBER(4),
    Genre           VARCHAR2(100),
    CopiesAvailable NUMBER,
    Cover           VARCHAR2(100)
);

CREATE TABLE Loans (
    LoanID     NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) PRIMARY KEY,
    BookID     REFERENCES Books(BookID),
    MemberID   REFERENCES Members(MemberID),
    StaffID    REFERENCES Staff(StaffID),
    IssueDate  DATE DEFAULT SYSDATE,
    DueDate    DATE,
    ReturnDate DATE
);

CREATE TABLE Fines (
    FineID      NUMBER GENERATED BY DEFAULT AS IDENTITY (START WITH 1 INCREMENT BY 1) PRIMARY KEY,
    LoanID      REFERENCES Loans(LoanID),
    MemberID    REFERENCES Members(MemberID),
    StaffID    REFERENCES Staff(StaffID),
    AmountDue   NUMBER(8,2),
    AmountPaid  NUMBER(8,2),
    PaymentDate DATE
);
-- CREATE ALL TABLES END




