-- Procedure that checks out a book for a member
create or replace PROCEDURE CheckOutBook (
    p_BOOKID NUMBER,
    p_MEMBERID NUMBER,
    p_STAFFID NUMBER
)
AS
BEGIN
    INSERT INTO Loans(BOOKID, MEMBERID, STAFFID, ISSUEDATE)
    VALUES (p_BOOKID, p_MEMBERID, p_STAFFID, SYSDATE);
END;
/

